#include <stdio.h>

int main() {
    printf("===== w06-02-01 =====\n");

    int i = 1, j = 2, k; 
    printf("1: i = %d, j = %d, k = ?\n", i, j);

    k = i + j;
    printf("2: i = %d, j = %d, k = %d\n", i, j, k);

    i = i + (k * j);
    printf("3: i = %d, j = %d, k = %d\n", i, j, k);

    j = i / 2;
    printf("4: i = %d, j = %d, k = %d\n", i, j, k);

    k = i % 2;
    printf("5: i = %d, j = %d, k = %d\n", i, j, k);

    i = (j + k) * 3;
    printf("6: i = %d, j = %d, k = %d\n", i, j, k);

    printf("\n===== w06-02-02 =====\n");

    double x = 1.0, y = 2.0, z;
    printf("1: x = %.1f, y = %.1f, z = ?\n", x, y);

    x = y + 5.0;
    printf("2: x = %.1f, y = %.1f, z = ?\n", x, y);

    y = x / 2.0;
    printf("3: x = %.1f, y = %.1f, z = ?\n", x, y);

    y = (x * 3.0) + 4.0;
    printf("4: x = %.1f, y = %.1f, z = ?\n", x, y);

    x = -0.5 - y;
    printf("5: x = %.1f, y = %.1f, z = ?\n", x, y);

    z = x + y;
    printf("6: x = %.1f, y = %.1f, z = %.1f\n", x, y, z);

    return 0;
}
//w06-02-01
//กำหนดค่าตัวแปร i เป็น 1 และ j เป็น 2 ส่วน k ยังไม่ได้กำหนดค่า
//นำค่า i และ j มาบวกกัน แล้วเก็บผลลัพธ์ไว้ในตัวแปร k ซึ่งได้ค่า k = 3
//ต่อมา นำค่า k คูณกับ j แล้วนำผลลัพธ์ไปบวกกับ i เดิม ซึ่งได้ i = 1 + (3 * 2) = 7
//แล้วนำค่า i ไปหาร 2 โดยใช้การหารแบบจำนวนเต็ม (ปัดเศษทิ้ง) ได้ค่า j = 3
//นำค่า i ไปหารเอาเศษด้วย 2 ได้ค่า k = 1 (เพราะ 7 หาร 2 เหลือเศษ 1)
//สุดท้าย นำ j กับ k มาบวกกันได้ 4 แล้วคูณด้วย 3 ได้ค่า i = 12

//w06-02-02
//เริ่มต้นโดยกำหนดค่า x = 1.0 และ y = 2.0 เป็นค่าทศนิยม ส่วน z ยังไม่ถูกกำหนด
//บรรทัดถัดไปนำ y ไปบวกกับ 5.0 แล้วเก็บไว้ใน x ดังนั้น x = 7.0
//นำ x ไปหาร 2.0 แล้วเก็บไว้ใน y ได้ y = 3.5
//แล้วนำ x ไปคูณกับ 3.0 ได้ 21.0 จากนั้นบวกกับ 4.0 เป็น 25.0 แล้วเก็บผลลัพธ์นี้ไว้ใน y
//จากนั้นนำ -0.5 ไปลบด้วยค่า y ซึ่งก็คือ -0.5 - 25.0 = -25.5 แล้วเก็บไว้ใน x
//สุดท้าย นำ x และ y มาบวกกัน (-25.5 + 25.0) ได้ผลลัพธ์เป็น -0.5 แล้วเก็บไว้ใน z